#version 330 core
// ===========================================================================
// shader.fs —— 片段着色器 (Fragment Shader)
// ---------------------------------------------------------------------------
// 角色：对光栅化后覆盖三角形的每一个像素（片段）都执行一次，
//       决定最终写入颜色缓冲的 RGBA 颜色。
// ===========================================================================

// 最终输出的片段颜色 (RGBA)；名字任意，只要有且仅有一个 vec4 类型的 out 变量
out vec4 FragColor;

// 来自顶点着色器的 vertexColor，会在光栅化阶段按三角形重心坐标自动插值
in  vec3 vertexColor;

void main() {
    // 把插值后的 RGB 颜色补上 alpha=1.0，作为最终像素颜色写入帧缓冲
    FragColor = vec4(vertexColor, 1.0);
}
